import { Resend } from 'resend';
import type { BetDecision } from './decision';

const resend = new Resend(process.env.RESEND_API_KEY);

export interface PickEmailData {
  homeTeam: string;
  awayTeam: string;
  league: string;
  kickoff: string;
  market: string;
  odds?: number;
  confidence: number;
  edgePct?: number;
  reason: string;
}

function leagueLabel(slug: string) {
  const map: Record<string, string> = {
    'premier-league': 'Premier League',
    'la-liga': 'La Liga',
    'serie-a': 'Serie A',
    'bundesliga': 'Bundesliga',
    'ligue-1': 'Ligue 1',
  };
  return map[slug] ?? slug.replace(/-/g, ' ');
}

function pickRow(p: PickEmailData, i: number) {
  const kickoff = new Date(p.kickoff);
  const timeStr = kickoff.toLocaleTimeString('en-GB', { hour: '2-digit', minute: '2-digit' });
  const dateStr = kickoff.toLocaleDateString('en-GB', { weekday: 'short', day: 'numeric', month: 'short' });
  const confColor = p.confidence >= 75 ? '#10b981' : '#f59e0b';

  return `
    <tr>
      <td style="padding:14px 0;border-bottom:1px solid #1e1e30;">
        <table width="100%" cellpadding="0" cellspacing="0">
          <tr>
            <td>
              <div style="font-size:10px;color:#52526e;text-transform:uppercase;letter-spacing:0.08em;margin-bottom:2px;">
                #${i + 1} · ${leagueLabel(p.league)} · ${dateStr} ${timeStr}
              </div>
              <div style="font-size:15px;font-weight:700;color:#f1f1f8;margin-bottom:4px;">
                ${p.homeTeam} vs ${p.awayTeam}
              </div>
              <div style="font-size:13px;color:#06b6d4;font-weight:600;margin-bottom:4px;">
                ${p.market}${p.odds ? ` <span style="color:#8b8ba8;font-weight:400;">@ ${p.odds.toFixed(2)}</span>` : ''}
              </div>
              <div style="font-size:12px;color:#8b8ba8;line-height:1.5;">${p.reason}</div>
            </td>
            <td style="text-align:right;vertical-align:top;padding-left:12px;white-space:nowrap;">
              <div style="font-size:20px;font-weight:800;color:${confColor};">${p.confidence}%</div>
              ${p.edgePct !== undefined ? `<div style="font-size:11px;color:#10b981;">+${p.edgePct}% edge</div>` : ''}
            </td>
          </tr>
        </table>
      </td>
    </tr>
  `;
}

