export async function sendPicksAlert(picks: PickEmailData[], toEmail: string) {
  const date = new Date().toLocaleDateString('en-GB', {
    weekday: 'long', day: 'numeric', month: 'long', year: 'numeric',
  });

  const { data, error } = await resend.emails.send({
    from: 'GoalEdge <onboarding@resend.dev>',
    to: toEmail,
    subject: `⚽ ${picks.length} GoalEdge ${picks.length === 1 ? 'Pick' : 'Picks'} for ${date}`,
    html: buildEmailHtml(picks, date),
  });

  if (error) throw new Error(error.message);
  return data;
}
